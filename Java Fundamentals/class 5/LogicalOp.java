class LogicalOp{

  public static void main(String[] args){
	
	boolean a = false;
	boolean b = true;

	System.out.println("a && b: " + (a && b));  // output: false
	System.out.println("a || b: " + (a || b)); // output: true
	System.out.println("!(a && b): " + (!(a && b))); // output:true

		

  }
}


